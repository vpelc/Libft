#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	*s;
	size_t	i;

	s = ft_itoa(n);
	i = 0;
	while (s[i] != '\0')
	{
		write(fd, &s[i], sizeof(char));
		i++;
	}
}
